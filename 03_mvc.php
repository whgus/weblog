03-1 화면 표시 View : MVC의 V
View는 App관련 Action Controller에서 준비
View 클래스 입장에서 보면 App에서 어느 파일이 View파일에 해당하는지 알수 없다.

view파일 : views폴더 내에 작성
ex> Controller명 : account, Action명 : inputs
==> view파일 : views/account/inputs.php
(이 프레임 워크에서만의 규칙.)
Action Controller의 이름 : AccountController
Action Method명 : inputsAction()으로 정한다.

